/**
 *_isdigit- returns a nonzero value if c is a decimal digit 
 *@c: Returns 1 if c is uppercase
 *Return: Returns 1 if c is a digit Returns 0 otherwise
 */
int _isdigit(int c)

{
{
	if (c >= 'a' && c <= 'z')
{
	return (0);
}
	if (c >= 0 && c <= 9)
{
	return (1);
}
	return (1);
}
}
